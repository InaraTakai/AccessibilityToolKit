            Color("ToolKitBackground")
        }
        
        static var red: Color {
            Color("ToolKitRed")
        }
        
        static var blue: Color {
            Color("ToolKitBlue")
        }
        
        static var yellow: Color {
            Color("ToolKitYellow")
        }
        
        static var green: Color {
            Color("ToolKitGreen")
        }
    }
}
